import streamlit as st
import torch
from namesformerokas import NameDataset, MinimalTransformer, sample_with_temperature
import random
import numpy as np

# 1. Load datasets
male_dataset = NameDataset('male_names.txt')
female_dataset = NameDataset('female_names.txt')

# 2. Load pre-trained models
male_model = MinimalTransformer(vocab_size=male_dataset.vocab_size, embed_size=128, num_heads=8)
male_model.load_state_dict(torch.load('male_model.pth'))
male_model.eval()

female_model = MinimalTransformer(vocab_size=female_dataset.vocab_size, embed_size=128, num_heads=8)
female_model.load_state_dict(torch.load('female_model.pth'))
female_model.eval()

# 3. Streamlit UI
st.set_page_config(page_title="VardÅ³ Generatorius", layout="centered")
st.markdown(
    """
    <style>
    body {
        background-color: #f9f9f9;
        font-family: Arial, sans-serif;
    }
    h1, h2, h3 {
        color: #004d99;
    }
    .highlight {
        color: #ff5722;
        font-weight: bold;
    }
    </style>
    """,
    unsafe_allow_html=True,
)
st.title("ğŸ‰ VardÅ³ Generatorius")
st.write("Sukurkite unikalius vardus naudodami dirbtinio intelekto modelÄ¯!")

# 4. Gender Selection
gender = st.radio("Pasirinkite vardÅ³ tipÄ…:", ["VyriÅ¡ki vardai", "MoteriÅ¡ki vardai"])
model = male_model if gender == "VyriÅ¡ki vardai" else female_model
dataset = male_dataset if gender == "VyriÅ¡ki vardai" else female_dataset

# 5. Input Controls
start_str = st.text_input(
    "Ä®veskite pradÅ¾ios raides (iki 5):",
    value="",
    max_chars=5,
    help="Nurodykite vardo pradÅ¾ios raides (1â€“5 simboliai). Jei laukelis tuÅ¡Äias, atsitiktinai sugeneruojamas vardas."
)

# Example Starting Letters
if st.button("Pavyzdys: 'Jon'"):
    start_str = "Jon"
if st.button("Pavyzdys: 'Ana'"):
    start_str = "Ana"

# Random Name Generation
if len(start_str) == 0:
    start_str = random.choice(dataset.chars)

temperature = st.slider(
    "KÅ«rybingumo lygis (temperatÅ«ra):",
    min_value=0.1,
    max_value=2.0,
    value=1.0,
    help="MaÅ¾esnÄ— temperatÅ«ra suteikia tikslesnius rezultatus, didesnÄ— â€“ kÅ«rybiÅ¡kesnius."
)

# Favorites list
st.markdown("### ğŸŒŸ MÄ—gstami vardai")
favorites = st.session_state.get("favorites", [])  # Keep favorites across app reruns
if "favorites" not in st.session_state:
    st.session_state["favorites"] = []

# Generate Names
if st.button("Generuoti vardus"):
    st.write(f"Sugeneruoti vardai, pradedant nuo: **{start_str}**")

    try:
        # Generate the most probable name
        output = model(torch.tensor([[dataset.char_to_int[c] for c in start_str]]))
        most_probable_char_idx = torch.argmax(output[0, -1]).item()
        most_probable_name = start_str + dataset.int_to_char[most_probable_char_idx]

        st.markdown(f"### Pagrindinis vardas: <span class='highlight'>{most_probable_name}</span>", unsafe_allow_html=True)

        # Save to favorites
        if st.button(f"â­ PridÄ—ti Ä¯ mÄ—gstamus: {most_probable_name}"):
            st.session_state["favorites"].append(most_probable_name)

        # Generate other similar names
        st.markdown("#### PanaÅ¡Å«s vardai:")
        generated_names = []
        for _ in range(5):  # Generate 5 similar names
            name = sample_with_temperature(
                model,
                dataset,
                start_str=start_str,
                max_length=10,  # Fixed length
                k=5,  # Fixed Top-k
                temperature=temperature,
            )
            generated_names.append(name)
            if st.button(f"â­ PridÄ—ti Ä¯ mÄ—gstamus: {name}"):
                st.session_state["favorites"].append(name)
            st.write(f"- {name}")

        # Display Name Statistics
        st.markdown("### ğŸ“Š VardÅ³ statistika")
        st.write(f"Vidutinis ilgis: {np.mean([len(name) for name in generated_names]):.2f} simboliai")
        st.write(f"UnikaliÅ³ vardÅ³ skaiÄius: {len(set(generated_names))}")

    except Exception as e:
        st.error(f"Klaida generuojant vardus: {e}")

# Display favorites
if favorites:
    st.markdown("#### IÅ¡saugoti mÄ—gstami vardai:")
    for favorite in favorites:
        st.write(f"- {favorite}")
    # Download favorites
    if st.button("AtsisiÅ³sti mÄ—gstamus vardus"):
        favorites_text = "\n".join(favorites)
        st.download_button("AtsisiÅ³sti", favorites_text, "favorites.txt", "text/plain")

    else:
        st.warning("Ä®veskite bent vienÄ… raidÄ™, kad sugeneruotumÄ—te vardus.")

